            <h2>What is a 401(k)? </h2>
            <p> A 401(k) is a retirement savings plan that is offered by employers. This plan allows employees to contribute a portion of their pre-tax income into their 401k account which can grow tax-deferred until withdrawn from during retirement.</p>
            <p>Contributions are deducted from your paycheck before taxes are taken out, which reduces your taxable income for the year. This can help reduce the taxes you owe at the end of the year depedending on your tax threshold.</p>
            <h2>When can I open a 401(k)?</h2>
            <p>Normally if your employer offers a 401(k) you can enroll and begin making contributions during open enrollment. 401(k)s are usually offered to full-time employees and sometimes part-time employees all dependent on the employer. It is important to ask and find out the benefits your employer can provide to you.</p>
            <h2>Self-Employed 401(k)?</h2>
            <p>If you are self-employed or if your employer does not offer a 401(k) you may be able to open an individual 401(k) plan also known as solo 401(k).</p>
            <p>Individual 401(k) plans have contribution limits and rules. You can contribute up to a certain percentage of your own income, up to an annual limit, and similar to a regular 401(k), contributions grow tax-deferred until withdrawn during retirement.</p>
            <h2>Withdrawals from a 401(k)?</h2>
            <p>Withdrawals from a 401(k) are generally taxed as ordinary income at retirement however, if attempting to withdraw before retirement, it will be taxed as income and owe a 10% early withdrawal penalty. </p>
            <p>It is never recommended to withdraw from your  401k unless it is necessary due to a financial hardship. Speaking with a financial advisor or tax professional is important before withdrawing from your 401k.</p>
            <h2>Investment options</h2>
            <p>401(k) plans usually offer a wide variety of investment options such as mutual funds, target-date funds, exchange-traded funds and more. Some 401(k) allow you to loan yourself money from your own 401(k)in which you pay back. However, losing employment will require to pay the loan in full directly or be taxed as a ordinary income on the withdraw.</p>

